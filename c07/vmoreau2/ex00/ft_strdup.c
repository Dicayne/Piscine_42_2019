#include <stdlib.h>

int		ft_strlen(char *src)
{
	int i;

	if (src == NULL)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	return (i);
}

char	*ft_strdup(char *src)
{
	char	*dup;
	int		i;

	if (src == NULL)
		return (0);
	dup = (char *)malloc((ft_strlen(src) + 1) * sizeof(char));
	if (dup == NULL)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
